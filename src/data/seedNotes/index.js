/**
 * Get seed note content for a given noteId.
 * NoteId format: "note:subject:unitId:topicId:subtopicIndex"
 * @param {string} noteId
 * @returns {Object|null}
 */
export function getSeedNote(noteId) {
    // noteId = "note:chemistry:1:1:0"
    const parts = String(noteId).split(':');
    if (parts.length !== 5 || parts[0] !== 'note') return null;
    const key = `${parts[1]}:${parts[2]}:${parts[3]}:${parts[4]}`;
    return seedNotes[key] || null;
}
